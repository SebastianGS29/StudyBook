package com.example.admin_login_backend.entity;

public class GestionSalas {
    private int computadora;
    private boolean detalle;
    private String encargado;
    private int mesa;
    private int mota;
    private String nombre;
    private int pizarra;
    private int plumon;
    private int sillas;

    public int getComputadora() {
        return computadora;
    }

    public void setComputadora(int computadora) {
        this.computadora = computadora;
    }

    public boolean isDetalle() {
        return detalle;
    }

    public void setDetalle(boolean detalle) {
        this.detalle = detalle;
    }

    public String getEncargado() {
        return encargado;
    }

    public void setEncargado(String encargado) {
        this.encargado = encargado;
    }

    public int getMesa() {
        return mesa;
    }

    public void setMesa(int mesa) {
        this.mesa = mesa;
    }

    public int getMota() {
        return mota;
    }

    public void setMota(int mota) {
        this.mota = mota;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPizarra() {
        return pizarra;
    }

    public void setPizarra(int pizarra) {
        this.pizarra = pizarra;
    }

    public int getPlumon() {
        return plumon;
    }

    public void setPlumon(int plumon) {
        this.plumon = plumon;
    }

    public int getSillas() {
        return sillas;
    }

    public void setSillas(int sillas) {
        this.sillas = sillas;
    }
}
